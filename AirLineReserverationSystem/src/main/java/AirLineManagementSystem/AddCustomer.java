    public void autoID(){
            try {
                try {
                    Class.forName("com.mysql.jdbc.Driver");
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
                con=(Connection)DriverManager.getConnection("jdbc:mysql://localhost/airlinemanagementsystem","root", "Y1012Jqkhkp");
                java.sql.Statement s = con.createStatement();
                ResultSet rs = s.executeQuery("Select MAX(CustomerId) from Customers");
                rs.next();
                 
                if(rs.getString("MAX(CustomerId)")==null){
                    CustomerId.setText("IND/CS/0001");
                }
                else{
                    long id = Long.parseLong(rs.getString("MAX(CustomerId)").substring(8,rs.getString("MAX(CustomerId").length()));
                    id++;
                    CustomerId.setText("IND/CS/"+String.format("%04d", id));
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
    
