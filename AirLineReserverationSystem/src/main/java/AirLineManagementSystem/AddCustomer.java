    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed

                String FirstName = firstName.getText();
                String LastName = lastName.getText();
                String Passport = passPort.getText();
                String Address = address.getText();
                String NICId = nicID.getText();
                String Contact = contact.getText();
                String Pincode = pincode.getText();
                //Date d = (Date) date.getDate();
                String Gender = "";
                
                if(male.isSelected())
                    Gender = "Male";
                else if(female.isSelected()) 
                    Gender = "Female";
                else
                    Gender = "Others";
                String CustomerID = CustomerId.getText();
                
                DateFormat dt = new SimpleDateFormat("yyyy-MM-dd");
                String date = dt.format(dob.getDate());
                try {
                    try {
                        Class.forName("con.mysql.jdbc.Driver");
                    } catch (ClassNotFoundException ex) {
                        Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    con = (Connection)DriverManager.getConnection("jdbc:mysql://localhost/airlinemanagementsystem","root", "Y1012Jqkhkp");
                    pre = con.prepareStatement("insert into Customers(customerId,FirstName, LastName, Passport, NICId, date, Mob_No, Address, Gender)values(?,?,?,?,?,?,?,?,?,?)");
                    pre.setString(1, CustomerID);
                    pre.setString(2, FirstName);
                    pre.setString(3, LastName);
                    pre.setString(4, Passport);
                    pre.setString(5, NICId);
                    pre.setString(6, date);
                    pre.setString(7, Contact);
                    pre.setString(8, Address);
                    pre.setString(9, Gender);
                    pre.setString(10, Pincode);
                    
                    pre.executeUpdate();
                    JOptionPane.showMessageDialog(null, "Customer Added!!!");
                    
                } catch (SQLException ex) {
                    Logger.getLogger(AddCustomer.class.getName()).log(Level.SEVERE, null, ex);
                }
    }//GEN-LAST:event_saveActionPerformed

